package nenu;

public class Swap 
{
	public static void main(String[] args) 
	{
		int a = 10;
		int b = 20;
		
		int c = a; //use 3rd variable like EX: 3 match boxes 2 match sticks 
		    a = b;
		    b = c;
		
		System.out.println("After swap A: " + a);
		System.out.print("After swap B: " + b);
	}
}
